document.addEventListener('DOMContentLoaded', () => {
    const counterElement = document.getElementById('co');
    let currentNumber = 1;
    
    function updateCounter() {
        counterElement.textContent = currentNumber;
        currentNumber++;
        if (currentNumber > 12) {
            clearInterval(intervalId); 
        }
    }
    
   
    const intervalId = setInterval(updateCounter, 120);
});


